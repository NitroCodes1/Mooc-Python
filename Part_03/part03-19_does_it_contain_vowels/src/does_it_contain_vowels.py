word = str(input("Please type in a string:"))
vowel1 = 'a'
vowel2 = 'e'
vowel3 = 'o'

if vowel1 in word:
    print("a found")
else:
    print("a not found")
if vowel2 in word:
    print("e found")
else:
    print("e not found")
if vowel3 in word:
    print("o found")
else:
    print("o not found")
    

